/**
 * Binary search to find the index of the largest value <= target
 */
function findLowerIndex(arr: number[], target: number): number {
    if (target <= arr[0]) return 0;
    if (target >= arr[arr.length - 1]) return arr.length - 2;

    let low = 0;
    let high = arr.length - 1;

    while (low < high - 1) {
        const mid = Math.floor((low + high) / 2);
        if (arr[mid] <= target) {
            low = mid;
        } else {
            high = mid;
        }
    }
    return low;
}

/**
 * Bilinear interpolation for 2D lookup tables
 * 
 * @param x - First dimension value (temperature in K)
 * @param y - Second dimension value (pressure in Pa)
 * @param xArr - Array of x values (temperatures)
 * @param yArr - Array of y values (pressures)  
 * @param zArr - 2D array of z values [y][x] (Ksh factors)
 * @returns Interpolated z value
 */
function bilinearInterpolate(
    x: number,
    y: number,
    xArr: number[],
    yArr: number[],
    zArr: number[][]
): number {
    // Clamp to bounds
    const xClamped = Math.max(xArr[0], Math.min(x, xArr[xArr.length - 1]));
    const yClamped = Math.max(yArr[0], Math.min(y, yArr[yArr.length - 1]));

    // Find indices
    const i = findLowerIndex(xArr, xClamped);
    const j = findLowerIndex(yArr, yClamped);

    // Get bounding values
    const x1 = xArr[i];
    const x2 = xArr[Math.min(i + 1, xArr.length - 1)];
    const y1 = yArr[j];
    const y2 = yArr[Math.min(j + 1, yArr.length - 1)];

    // Get corner values (z[pressure][temperature])
    const z11 = zArr[j][i];
    const z12 = zArr[j][Math.min(i + 1, xArr.length - 1)];
    const z21 = zArr[Math.min(j + 1, yArr.length - 1)][i];
    const z22 = zArr[Math.min(j + 1, yArr.length - 1)][Math.min(i + 1, xArr.length - 1)];

    // Handle edge cases where x1 === x2 or y1 === y2
    if (x2 === x1 && y2 === y1) {
        return z11;
    }
    if (x2 === x1) {
        // Linear interpolation in y only
        const t = (yClamped - y1) / (y2 - y1);
        return z11 + t * (z21 - z11);
    }
    if (y2 === y1) {
        // Linear interpolation in x only
        const t = (xClamped - x1) / (x2 - x1);
        return z11 + t * (z12 - z11);
    }

    // Bilinear interpolation
    const tx = (xClamped - x1) / (x2 - x1);
    const ty = (yClamped - y1) / (y2 - y1);

    const z1 = z11 + tx * (z12 - z11);
    const z2 = z21 + tx * (z22 - z21);

    return z1 + ty * (z2 - z1);
}

/**
 * Calculate the superheat correction factor Ksh for steam service
 * per API-520.
 * 
 * @param T1 - Temperature of steam entering the valve [K]
 * @param P1 - Upstream relieving pressure (absolute) [Pa]
 * @param edition - API-520 edition ('7E' or '10E')
 * @returns Ksh correction factor (1.0 for saturated steam)
 * 
 * @example
 * // Superheated steam at 593째C, 10.66 bar absolute (7th edition)
 * const Ksh = calculateKsh(593 + 273.15, 1066325, '7E');
 * // Returns approximately 0.72
 */
export function calculateKsh(T1: number, P1: number, edition: APIEdition = '10E'): number {
    if (edition === '7E') {
        // 7th Edition limits
        if (T1 > 922.15) {
            throw new Error('Superheat cannot be above 649째C (922.15 K)');
        }
        if (P1 > 20780325) {
            throw new Error('For P1 above 20679 kPag, use the gas flow model');
        }
        if (T1 < 422.15) {
            return 1.0; // No superheat below saturation temperature
        }
        return bilinearInterpolate(T1, P1, KSH_K_7E, KSH_Pa_7E, KSH_FACTORS_7E);
    } else if (edition === '10E') {
        // 10th Edition limits
        if (T1 > 922.15) {
            throw new Error('Superheat cannot be above 649째C (922.15 K)');
        }
        if (P1 > 22063223) {
            throw new Error('For P1 above 22.06 MPa, use the gas flow model');
        }
        if (T1 < 478.15) {
            return 1.0; // Avoid extrapolating above 1.0
        }
        return bilinearInterpolate(T1, P1, KSH_K_10E, KSH_Pa_10E, KSH_FACTORS_10E);
    } else {
        throw new Error("Acceptable editions are '7E' or '10E'");
    }
}
